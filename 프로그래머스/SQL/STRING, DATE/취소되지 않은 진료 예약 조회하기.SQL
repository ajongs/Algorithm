SELECT A.APNT_NO, 
      P.PT_NAME,
      P.PT_NO,
      D.MCDP_CD,
      D.DR_NAME, 
      A.APNT_YMD
FROM APPOINTMENT A
JOIN PATIENT P 
ON A.PT_NO = P.PT_NO
JOIN DOCTOR D
ON D.DR_ID = A.MDDR_ID
WHERE APNT_YMD LIKE "2022-04-13%"
AND D.MCDP_CD = 'CS'
AND APNT_CNCL_YN = 'N'
ORDER BY APNT_YMD
